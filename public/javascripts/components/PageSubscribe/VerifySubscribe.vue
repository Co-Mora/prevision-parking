export default {
  name: 'AddLevel',
  data () {
    return {
      userData: null,
      scode: null,
      mobile: null,
      validated: false,
      errors: [],
      clicked: [],
      token: localStorage.getItem('token'),
      isLoggedIn: localStorage.getItem('isLogged'),
    }
  },
  components: {
    NavSide
  },
  methods: {

    verifySubscribe() {
         setTimeout(() => {
        $('.alert').alert('close')
      }, 2000)
        if (!this.scode && !this.mobile) {
           this.errors.push('Please fill up the forms')
           return false
      } if (!this.scode) {
        this.errors.push('Please fill up the Scode')
      } if (!this.mobile) {
        this.errors.push('Please fill up the User phone')
      } if(!this.mobile.startsWith('60')){
        this.errors.push('your phone number must start with 60')
      }else {
        this.errors = []
        this.validated = true;
        axios({
        method: 'post',
        url: `https://sys2.parkaidemobile.com/api/subscriber/subscribe`,
        data: qs.stringify({
            mobile: this.mobile,
            scode: this.scode
        }),
        headers: {
            'Content-Type': 'application/x-www-form-urlencoded',
            'x-access-token': JSON.parse(this.token)
        },
        }).then(response => {
           this.userData = [response.data]
           console.log(this.userData)
           if(response.status == 200) {
             console.log(response)
             console.log(this.userData)



             setTimeout(() => {
                 swal({
                     title: 'Add it successfully',
                     icon: 'success',
                 });
             }, 200)
            }


        })
        .catch(error => {
            if(error.message == 'Request failed with status code 401') {
                 setTimeout(() => {
                    swal({
                        title: 'Your or password is wrong',
                        icon: 'error'
                    })
                }, 1000)
            }

        });
      }
    },
    logout() {
      localStorage.removeItem('isLogged');
      localStorage.removeItem('token');
    }
  },

}
